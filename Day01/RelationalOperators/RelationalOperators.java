public class RelationalOperators{
    public static void main(String [] args){
        boolean b = 3 < 4;
        System.out.println("3 < 4 is " + b + "\n");
        boolean b1 = 3 > 4;
        System.out.println("3 > 4 is " + b1 + "\n");
        boolean b2 = 5 == 5;
        System.out.println("5 == 5 is " + b2 +"\n");
        boolean b3 = 4 == 6;
        System.out.println("4 == 6 is " + b3 +"\n");
        boolean b4 = 0 < 4;
        System.out.println("0 < 4 is " + b4 + "\n");
        boolean b5 = 2 <= 4;
        System.out.println("2 <= 4 is "+ b5 + "\n");
        boolean b6 = 6 < 4;
        System.out.println("6 < 4 is " + b6 + "\n");
        boolean b7 = 7 <= 10;
        System.out.println("7 <= 10 is "+ b7 +"\n");
        boolean b8 = 10 != 2;
        System.out.println("10 != 2 is " + b8);
    }
}